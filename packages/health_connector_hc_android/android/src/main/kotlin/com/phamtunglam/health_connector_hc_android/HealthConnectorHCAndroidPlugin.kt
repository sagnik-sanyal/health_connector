    /**
     * Deletes health records based on the request type.
     *
     * @param request The deletion request (either by IDs or time range)
     * @param callback Called with a [Result] indicating success or failure
     */
    override fun deleteRecords(
        request: DeleteRecordsRequestDto,
        callback: (Result<Unit>) -> Unit,
    ) {
        scope.launch {
            process(
                operation = "delete_records",
                context = mapOf("request_type" to request.javaClass.simpleName),
                callback = callback,
            ) {
                val healthClient = requireClient()
                when (request) {
                    is DeleteRecordsByIdsRequestDto -> healthClient.deleteRecordsByIds(request)
                    is DeleteRecordsByTimeRangeRequestDto -> healthClient.deleteRecordsByTimeRange(
                        request,
                    )
                }
            }
        }
    }

