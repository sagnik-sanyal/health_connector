    // region Plugin API

    /**
     * Initializes the Health Connector client with the provided configuration.
     *
     * This method must be called before any other Health Connector operations
     * to properly configure the native platform code, including logger settings.
     *
     * @param config Configuration settings for the Health Connector
     * @param callback Called with a [Result] indicating success or failure
     */
    override fun initialize(config: HealthConnectorConfigDto, callback: (Result<Unit>) -> Unit) {
        scope.launch {
            HealthConnectorLogger.debug(
                "HealthConnectorHCAndroidPlugin",
                operation = "initialize",
                message = "Initializing Health Connector Android plugin...",
                context = mapOf("is_logger_enabled" to config.isLoggerEnabled.toString()),
            )

            try {
                clientInitMutex.withLock {
                    if (client == null) {
                        client = HealthConnectorClient.getOrCreate(context)
                    }
                }

                HealthConnectorLogger.isEnabled = config.isLoggerEnabled

                HealthConnectorLogger.debug(
                    "HealthConnectorHCAndroidPlugin",
                    operation = "initialize",
                    message = "Initialized Health Connector Android plugin.",
                    context = mapOf("is_logger_enabled" to config.isLoggerEnabled.toString()),
                )

                callback(Result.success(Unit))
            } catch (e: HealthConnectorException) {
                HealthConnectorLogger.error(
                    "HealthConnectorHCAndroidPlugin",
                    operation = "initialize",
                    message = "Failed to initialize Health Connector Android plugin.",
                    context = mapOf(
                        "error_code" to e.code,
                        "error_message" to (e.message ?: "Unknown error"),
                    ),
                    exception = e,
                )

                callback(Result.failure(e))
            } catch (e: CancellationException) {
                throw e
            } catch (e: Exception) {
                HealthConnectorLogger.error(
                    "HealthConnectorHCAndroidPlugin",
                    operation = "create",
                    message = "Unexpected error during initialization Health Connector " +
                        "Android plugin.",
                    context = mapOf("is_logger_enabled" to config.isLoggerEnabled.toString()),
                    exception = e,
                )
                callback(
                    Result.failure(
                        HealthConnectorException.Unknown(
                            "Unexpected error: ${e.message ?: "Unknown error"}",
                            cause = e,
                        ).toDto(),
                    ),
                )
            }
        }
    }

    /**
     * Launches the Health Connect app page in the Google Play Store.
     *
     * Opens the Google Play Store application to the Health Connect app's detail page,
     * allowing users to install or update the app. This method should be called when
     * [getHealthPlatformStatus] indicates that Health Connect installation or update is required.
     *
     * @param callback Called with a [Result] indicating success or failure
     *
     * @throws HealthConnectorErrorDto if the Play Store cannot be launched or activity is unavailable
     */
    override fun launchHealthConnectPageInGooglePlay(callback: (Result<Unit>) -> Unit) {
        try {
            val currentActivity = activity
            if (currentActivity == null) {
                HealthConnectorLogger.error(
                    "HealthConnectorHCAndroidPlugin",
                    operation = "launch_health_connect_page_in_google_play",
                    message = "Cannot launch Play Store without activity context",
                )

                callback(
                    Result.failure(
                        HealthConnectorException.InvalidConfiguration(
                            message = "Activity is unavailable. " +
                                "The app may be in the background or " +
                                "activity has been destroyed.",
                        ).toDto(),
                    ),
                )

                return
            }

            HealthConnectorClient.launchHealthConnectPageInGooglePlay(currentActivity)

            callback(Result.success(Unit))
        } catch (e: HealthConnectorException) {
            HealthConnectorLogger.error(
                "HealthConnectorHCAndroidPlugin",
                operation = "launch_health_connect_page_in_google_play",
                message = "Failed to launch Health Connect page in Google Play",
                context = mapOf(
                    "error_code" to e.code,
                    "error_message" to (e.message ?: "Unknown error"),
                ),
                exception = e,
            )

            callback(Result.failure(e))
        } catch (e: Exception) {
            HealthConnectorLogger.error(
                "HealthConnectorHCAndroidPlugin",
                operation = "launch_health_connect_page_in_google_play",
                message = "Unexpected error while launching Play Store",
                exception = e,
            )

            callback(
                Result.failure(
                    HealthConnectorException.Unknown(
                        message = "Failed to launch Play Store: ${e.message ?: "Unknown error"}",
                        cause = e,
                    ).toDto(),
                ),
            )
        }
    }

