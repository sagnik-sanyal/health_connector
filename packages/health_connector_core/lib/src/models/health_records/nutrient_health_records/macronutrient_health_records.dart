part of '../health_record.dart';

/// Base class for macronutrient health records.
@sinceV1_1_0
@internal
@immutable
sealed class MacronutrientRecord extends NutrientHealthRecord<Mass> {
  const MacronutrientRecord({
    required super.value,
    required super.time,
    required super.metadata,
    super.id = HealthRecordId.none,
    super.zoneOffsetSeconds,
    super.foodName,
    super.mealType,
  });
}

/// Health record for protein intake.
@sinceV1_1_0
@immutable
final class ProteinNutrientRecord extends MacronutrientRecord {
  factory ProteinNutrientRecord({
    required Mass value,
    required DateTime time,
    required Metadata metadata,
    HealthRecordId id = HealthRecordId.none,
    int? zoneOffsetSeconds,
    String? foodName,
    MealType mealType = MealType.unknown,
  }) {
    return ProteinNutrientRecord._(
      value: value,
      time: time,
      metadata: metadata,
      id: id,
      zoneOffsetSeconds: zoneOffsetSeconds,
      foodName: foodName,
      mealType: mealType,
    );
  }

  const ProteinNutrientRecord._({
    required super.value,
    required super.time,
    required super.metadata,
    super.id = HealthRecordId.none,
    super.zoneOffsetSeconds,
    super.foodName,
    super.mealType,
  });

  @override
  String get name => 'protein_nutrient_record';
}

